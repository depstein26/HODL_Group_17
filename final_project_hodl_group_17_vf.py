"""**Cell 19 — Extract BERT Embeddings**

We pass every plot description through BERT's backbone and extract the CLS token embedding — a 768-dimensional vector at position 0 of the output sequence that serves as a summary representation of the entire input text. Unlike BoW, this vector captures the contextual meaning of the plot, not just which words appear.
We process texts in small batches (32 at a time) to avoid running out of GPU memory, and explicitly free memory after each batch.
"""

import gc
import tensorflow as tf

BATCH_SIZE = 32

def get_bert_embeddings(texts, batch_size=BATCH_SIZE):
    """
    Extract CLS token embeddings from BERT backbone in batches.
    Each text is tokenized by BERT's preprocessor, passed through the
    backbone encoder, and the first token's output (the CLS token)
    is kept as the 768-dim representation of the entire text.
    """
    all_emb = []
    n = len(texts)
    for i in range(0, n, batch_size):
        batch = texts[i:i+batch_size]
        # Tokenize the batch using BERT's built-in preprocessor
        tok = bert.preprocessor(batch)
        # Run through the backbone and extract the CLS token (position 0)
        emb = bert.backbone.predict(tok, verbose=0)['sequence_output'][:, 0, :]
        all_emb.append(emb)
        # Free memory after each batch to prevent OOM
        del tok, emb
        if (i // batch_size) % 100 == 0:
            print(f"  {min(i+batch_size, n):,} / {n:,}")
    return np.concatenate(all_emb, axis=0)

