"""##**Part 1 — Bag-of-Words Model**

With our data cleaned, features encoded, and train/test split verified, we now build our first model using the Bag-of-Words approach for text representation. In this method, each plot description is converted into a binary vector of length 10,000, where each position corresponds to one of the most frequent words in the training corpus.

Our BoW vector is combined with the genre, director, and lead actor branches in a multi-input neural network, and we perform a hyperparameter search to identify the best architecture before evaluating the winning model on the test set.

**Cell 12 — Text Vectorization (Bag-of-Words)**

We convert the plot descriptions into numerical vectors using Keras's TextVectorization layer. This implements a Bag-of-Words representation: each description becomes a binary vector of length max_tokens, where a 1 indicates that the corresponding word appears in the text. As we saw in Cell 6, the full vocabulary contains tens of thousands of unique words, but the top 10,000 cover the vast majority of word occurrences — the remaining words appear too rarely to carry predictive value. We use unigrams (single words), lowercase normalization, and punctuation stripping. The vocabulary is adapted on the training set only to prevent data leakage, then applied to both training and test sets.
"""

# Vocabulary size set to 10000 based on the coverage analysis in Cell 6.
# The top 10000 words capture the vast majority of word occurrences
# while discarding rare words that appear only once or twice.
max_tokens = 10000

text_vectorization = keras.layers.TextVectorization(
    max_tokens=max_tokens,
    standardize='lower_and_strip_punctuation',
    ngrams=None,          # unigrams only
    split='whitespace',
    output_mode='multi_hot'
)

# Build the vocabulary from training descriptions only (no data leakage)
text_vectorization.adapt(plot_train)

# Transform both sets into BoW vectors
X_text_train = text_vectorization(plot_train).numpy()
X_text_test  = text_vectorization(plot_test).numpy()

print(f"BoW vector shape (train): {X_text_train.shape}")
print(f"BoW vector shape (test):  {X_text_test.shape}")
print(f"Vocabulary size used:     {len(text_vectorization.get_vocabulary())}")

"""**Cell 13 — Hyperparameter Search and Final Model Selection**

We perform limited hyperparameter optimization over the model architecture. We test combinations of two hyperparameters:
1.   The number of neurons in the text branch (64, 128, or 256).
2.   The number of shared Dense layers after the merge (1 or 2).

All other parameters are held constant: vocabulary size 10000, genre/director/actor branch sizes 8 neurons* each, 20 epochs, batch size 64, dropout 0.2, and early stopping. This produces 6 combinations. Each one is trained silently and evaluated on the test set, and the results are collected into a sorted table to identify the best configuration.

Our model architecture uses separate branches for each input type — text, genre, director, and lead actor — each with a dedicated Dense layer. This allows the network to learn specialized representations before combining them via concatenation. After merging, one or two shared Dense layers learn how the combined features interact to determine whether a film is a Flop, Average, or Hit. The final layer uses softmax activation to output a probability distribution over the three classes.

Instead of discarding models after each run, we keep track of the best-performing one. Once the search is complete, the winning model is already trained and ready to use.

**NOTE: We intentionally constrain the genre, director, and actor branches to only 8 neurons to prevent the model from over-relying on categorical features like director or actor name. In early experiments with 32 neurons, the model effectively memorized "this director = Hit" lookup tables, producing near-100% confidence predictions driven almost entirely by the name rather than the plot. By limiting these branches to 8 neurons, the model cannot fully memorize name-outcome associations and is forced to rely more heavily on the plot description — which is the core NLP component of this project.*
"""

n_genres    = genre_train.shape[1]
n_directors = dir_train.shape[1]
n_actors    = actor_train.shape[1]

text_neurons_options  = [64, 128, 256]
shared_layers_options = [1, 2]

results = []
best_acc = 0
final_model = None
final_history = None

print("Running hyperparameter search (6 combinations)...")
print("-" * 55)

for text_neurons in text_neurons_options:
    for n_shared in shared_layers_options:

        t_in = keras.layers.Input(shape=(max_tokens,), name='text')
        t_x  = keras.layers.Dense(text_neurons, activation='relu')(t_in)

        g_in = keras.layers.Input(shape=(n_genres,), name='genre')
        g_x  = keras.layers.Dense(8, activation='relu')(g_in)

        d_in = keras.layers.Input(shape=(n_directors,), name='director')
        d_x  = keras.layers.Dense(8, activation='relu')(d_in)

        a_in = keras.layers.Input(shape=(n_actors,), name='actor')
        a_x  = keras.layers.Dense(8, activation='relu')(a_in)

        m = keras.layers.Concatenate()([t_x, g_x, d_x, a_x])
        x = keras.layers.Dense(64, activation='relu')(m)
        x = keras.layers.Dropout(0.2)(x)
        if n_shared == 2:
            x = keras.layers.Dense(32, activation='relu')(x)
            x = keras.layers.Dropout(0.2)(x)

        out = keras.layers.Dense(1, activation='sigmoid')(x)

        hp_model = keras.Model(inputs=[t_in, g_in, d_in, a_in], outputs=out)
        hp_model.compile(optimizer='adam', loss='binary_crossentropy', metrics=['accuracy'])

        hp_history = hp_model.fit(
            [X_text_train, genre_train, dir_train, actor_train], y_train,
            epochs=20, batch_size=64, validation_split=0.15,
            verbose=0
        )

        _, acc = hp_model.evaluate(
            [X_text_test, genre_test, dir_test, actor_test], y_test, verbose=0
        )

        results.append((text_neurons, n_shared, acc))
        print(f"  neurons={text_neurons}, shared_layers={n_shared}  →  accuracy: {acc:.2%}")

        # Keep the best model instead of deleting it
        if acc > best_acc:
            best_acc = acc
            final_model = hp_model
            final_history = hp_history
        else:
            del hp_model
            keras.backend.clear_session()

# Display sorted results
results_df = pd.DataFrame(results, columns=['Text Neurons', 'Shared Layers', 'Test Accuracy'])
results_df = results_df.sort_values('Test Accuracy', ascending=False).reset_index(drop=True)
print("\n" + "=" * 55)
print("HYPERPARAMETER SEARCH RESULTS")
print("=" * 55)
print(results_df.to_string(index=False))

best_neurons = int(results_df.iloc[0]['Text Neurons'])
best_shared  = int(results_df.iloc[0]['Shared Layers'])
print(f"\nBest: neurons={best_neurons}, shared_layers={best_shared}")
print(f"Test Accuracy: {best_acc:.2%}")
print(f"\nFinal model is already trained and ready to use.")

final_model.summary()

"""**Cell 14 — Final Model: Training Curves**

We plot the loss and accuracy curves for the final model across training epochs. The gap between the training and validation lines indicates how much the model is overfitting — if training accuracy climbs much higher than validation accuracy, the model is memorizing training data rather than learning generalizable patterns. Ideally, both lines converge and flatten, showing stable learning.
"""

fig, axes = plt.subplots(1, 2, figsize=(14, 5))

# Loss curve
axes[0].plot(final_history.history['loss'], label='Train')
axes[0].plot(final_history.history['val_loss'], label='Validation')
axes[0].set_title('Final Model — Loss (Categorical Crossentropy)')
axes[0].set_xlabel('Epoch'); axes[0].set_ylabel('Loss'); axes[0].legend()

# Accuracy curve
axes[1].plot(final_history.history['accuracy'], label='Train')
axes[1].plot(final_history.history['val_accuracy'], label='Validation')
axes[1].set_title('Final Model — Accuracy')
axes[1].set_xlabel('Epoch'); axes[1].set_ylabel('Accuracy'); axes[1].legend()

plt.tight_layout()
plt.show()

"""**Cell 15 — Final Model: Classification Report and Confusion Matrix**

Beyond overall accuracy, we evaluate per-class performance. Precision measures what fraction of films predicted as a given class actually belonged to that class. Recall measures what fraction of actual members of a class were correctly identified. F1-score is the harmonic mean of the two, providing a single balanced metric. The confusion matrix shows the full breakdown of predictions versus actual labels, making it easy to identify systematic errors — for example, whether the model tends to confuse Average films with Hits, which would be expected given the narrow boundary between those classes.
"""

# Generate predictions on the test set
y_pred_probs = final_model.predict([X_text_test, genre_test, dir_test, actor_test]).flatten()
y_pred = (y_pred_probs >= 0.5).astype(int)  # threshold at 0.5
y_true = y_test

print("Classification Report (Final Model):")
print("=" * 55)
print(classification_report(y_true, y_pred, target_names=label_names))

cm = confusion_matrix(y_true, y_pred)
disp = ConfusionMatrixDisplay(cm, display_labels=label_names)
fig, ax = plt.subplots(figsize=(6, 5))
disp.plot(cmap='Blues', ax=ax)
plt.title('Confusion Matrix (Final Model)')
